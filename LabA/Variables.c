#include <stdio.h>

int main(void) {

    // 1. Declaring Variables
    printf("Declaring Variables");
    int age = 18;
    float gpa = 1.00;
    char name[] = "Arbie";
    double speed = 27.5;

    printf("Age: %d\n", age); // Outputs 18
    printf("GPA: %.2f\n", gpa); // Outputs 1.00
    printf("Name: %s\n", name); // Outputs Arbie
    printf("Speed: %.2lf km/hr\n", speed); // Outputs 27.5 km/hr 

    printf("sample code");
    

    return 0;
}
